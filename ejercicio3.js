var valores = [true, 5, false, "hola", "adios", 2];

//Suma
console.log(valores[1]+valores[5]);

//Resta
console.log(valores[1]-valores[5]);

//Multiplicación
console.log(valores[1]*valores[5]);

//División
console.log(valores[1]/valores[5]);

//Resto
console.log(valores[1]%valores[5]);